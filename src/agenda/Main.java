    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:

        String seleccion = JOptionPane.showInputDialog(
                this,
                "Numero de Folio ", "ELIMINAR",
                JOptionPane.DEFAULT_OPTION);  // el icono sera un iterrogante
        if (!(seleccion == null)) {
            if (!seleccion.equals("") && seleccion.matches("[0-9]*")) {
                if (!lista.eliminarPersona(Integer.parseInt(seleccion))) {
                    mensajesJOption("Folio no encontrado",
                            "ERROR", JOptionPane.ERROR_MESSAGE, null);
                } else {
                    mensajesJOption("Se Elimino exitosamente", "Exito", JOptionPane.INFORMATION_MESSAGE,
                            new ImageIcon("src/recursos/exito.png"));
                    txtFolio.setText("");
                    txtNombre.setText("");
                    txtApellidos.setText("");
                    txtTelefono.setText("");
                    txtEdad.setText("");
                    lblMensajeAgendaVacia.setVisible(false);
                    lblMensajeAgendaVacia.setText("Ajenda Vacia ");
                    actualizacion();
                }

            } else {
                mensajesJOption("> No deje el campo vacio \n > No ingrese letras",
                        "ERROR", JOptionPane.ERROR_MESSAGE, null);
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        // TODO add your handling code here:
        String seleccion = JOptionPane.showInputDialog(
                this,
                "Numero de Folio ", "Buscar",
                JOptionPane.DEFAULT_OPTION);

        if (!(seleccion == null)) {
            if (!seleccion.equals("") && seleccion.matches("[0-9]*")) {

                int indice = lista.buscarPersona(Integer.parseInt(seleccion));
                if (indice != -1) {

                    Cliente envio = lista.itemAgenda(indice);

                    txtFolio.setText(String.valueOf(envio.getFolio()));
                    txtNombre.setText(envio.getNombre());
                    txtApellidos.setText(envio.getApellidos());
                    txtTelefono.setText(String.valueOf(envio.getTelefono()));
                    txtEdad.setText(String.valueOf(envio.getEdad()));
                    actualizacion();
                    elementoMostrado = indice;
                } else {
                    mensajesJOption(" Folio no encontrado",
                            "ERROR", JOptionPane.ERROR_MESSAGE, null);

                }
            } else {
                mensajesJOption("> No deje el campo vacio \n > No ingrese letras",
                        "ERROR", JOptionPane.ERROR_MESSAGE, null);
            }
        }


    }//GEN-LAST:event_btnBuscarActionPerformed

