    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (!txtFolio.getText().equals("") && !txtNombre.getText().equals("")
                && !txtApellidos.getText().equals("")
                && !txtTelefono.getText().equals("") && !txtEdad.getText().equals("")) {

            Cliente cliente = new Cliente();
            cliente.setFolio(Integer.parseInt(txtFolio.getText()));
            cliente.setNombre(txtNombre.getText());
            cliente.setApellidos(txtApellidos.getText());
            cliente.setTelefono(Long.parseLong(txtTelefono.getText()));
            cliente.setEdad(Integer.parseInt(txtEdad.getText()));
            if (!(lista.existeFolio(Integer.parseInt(txtFolio.getText())))) {
                if (lista.addLista(cliente)) {
                    // Mensaje de exito
                    mensajesJOption("Se ingreso exitosamente", "Exelnte", JOptionPane.INFORMATION_MESSAGE,
                            new ImageIcon("src/recursos/exito.png"));
                    JP_BotonesAgregar.setVisible(false);
                    txtFolio.setEditable(false);
                    txtNombre.setEditable(false);
                    txtApellidos.setEditable(false);
                    txtTelefono.setEditable(false);
                    txtEdad.setEditable(false);
                    txtFolio.setText("");
                    txtNombre.setText("");
                    txtApellidos.setText("");
                    txtTelefono.setText("");
                    txtEdad.setText("");
                }
            } else {
                mensajesJOption("Folio Repetido ", "ERROR", JOptionPane.ERROR_MESSAGE,
                        null);
            }
            actualizacion();
        } else {
            mensajesJOption("No deje campos vacios", "ERROR", JOptionPane.ERROR_MESSAGE,
                    null);
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

